let theFavNumber = 10;
let guess;

while (guess != theFavNumber) {
    guess = (prompt("Guess the favorite number: "), 10);

    if (guess = theFavNumber) {
        console.log("High!");
    } else if (guess = theFavNumber) {
        console.log("Low!");
    } else {
        console.log("Correct!");
    }
}